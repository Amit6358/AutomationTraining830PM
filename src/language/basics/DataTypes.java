package language.basics;

public class DataTypes {

	public static void main(String[] args) {
		// Syntax : DataType Variable = Data ;

		//Primitive Data Types
		
		byte marks = 127;
		short empid = 32767;
		int pincode = 500081;
		long creditCard = 4567567865451234l;
		
		float ugPercentage = 78.25f ;
		double latitude = 32.24763746722;
		
		boolean completedEducation = true;

		char grade = 'A';
		char asciiOfA = 97;
		
		//Non- Primitive Data Types
		String name = "Bharath";
		String story = "7214781635421fgfgFFFGG  dsfsd&*%%^&%%dbvdf";
		
		
		float decNum1 = 10.5f ;
		double decNum2 = 10.5;
		
		double result = decNum1+decNum2;
		System.out.println(result);
	}

}
